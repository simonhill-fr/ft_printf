#define CHAR_IS_MAJ (c >= 65 && c <= 90)
#define CHAR_IS_MIN (c >= 97 && c <= 122)

int		ft_isalpha(int c)
{
	if (!(CHAR_IS_MIN) && !(CHAR_IS_MAJ))
		return (0);
	else
		return (1);
}
