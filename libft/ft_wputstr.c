#include "libft.h"

int		ft_wputstr(unsigned int *wstr)
{
	int		i;
	int		ret;

	i = 0;
	ret = 0;
	while (wstr[i] != L'\0')
	{
		ret += ft_wputchar(wstr[i]);
			i++;
	}
	return (ret);
}
