#include "libft.h"

int	ft_putnbr_u(uintmax_t n)
{
	int	ret;

	ret = 0;
	if (n >= 10)
	{
		ret += ft_putnbr(n / 10);
		ret += ft_putnbr(n % 10);
	}
	else if (n < 10)
		ret += ft_putchar(n + '0');
	return (ret);
}
