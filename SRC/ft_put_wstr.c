int		ft_put_wstr(unsigned int *wstr)
{
	int		i;

	i = 0;
	if (wstr[3] == L'\0')
		printf("enter\n");
	while (wstr != L'\0')
	{
		ft_put_wchar(wstr[i]);
		i++;
	}
	return (i);
}
